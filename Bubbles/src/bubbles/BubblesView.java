    private void buildVisualization() 
    {
        //Draw graph line
        Graphics g=jPanel1.getGraphics();
        g.setColor(Color.GRAY);
        for(Integer i=20; i<=jPanel1.getSize().width; i=i+20)
        {
            g.drawLine(0, i, jPanel1.getSize().width,i);
            char[] lbl=i.toString().toCharArray();
            g.drawChars(lbl, 0, lbl.length, 0, i);
        }

        for(Integer i=20; i<=jPanel1.getSize().width; i=i+20)        
            g.drawLine(i, 0, i, jPanel1.getSize().height);
        

        jPanel1.paintComponents(g);

        //OUT WITH THE OLD, IN WITH THE NEW
        bManager.clearList();        
        bManager.readInputData(jTextPane1.getText(),jPanel1.getSize());

        
        //SORT BUBBLES AND PRINT BUBBLE INFO
        System.out.println("\nUNSORTED\n");
        bManager.printBubbles();
        bManager.sortBubbles();
        System.out.println("\nSORTED\n");
        bManager.printBubbles();

        //BLOW THOSE BUBBLES GUURRRL
        jPanel1.paintComponents(bManager.displayBubbles(jPanel1.getGraphics()));
    }

