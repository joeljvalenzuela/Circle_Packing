    @Action
    public Task ShowFileDialog() {
        jFileChooser1.showOpenDialog(mainPanel);
        File f =jFileChooser1.getSelectedFile();
        if(f.getAbsolutePath().contains(".txt"))
        {
        try
        {
            FileReader fReader= new FileReader(f);
            String input="";
            char[] cbuf=new char[1000];

            if(fReader.ready())
            {
                int end=1;
                while(end>0)
                   end=fReader.read(cbuf);

                for(int i=0; i<cbuf.length; i++)
                {
                    if(cbuf[i]=='\0')
                        break;
                    input=input+cbuf[i];
                }

                jTextPane1.setText("");
                jTextPane1.setText(input);
            }
        }
        catch(Exception e){System.out.println("Couldn't find File. \nError: "+e.getMessage());}}
    else
        {
            System.out.println("File is not a .txt file. Please try again.");
        }
        return new ShowFileDialogTask(getApplication());
    }

